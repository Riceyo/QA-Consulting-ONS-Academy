
public class MathMethods
{
	public int add(int a, int b)
	{
		int result = a + b;
		return result;
	}
	public int sub(int a, int b)
	{
		int result = a - b;
		return result;
	}
	public float div(int a, int b)
	{
		int result = a / b;
		return result;
	}
	public int mul(int a, int b)
	{
		int result = a * b;
		return result;
	}	
}
