
public class DifferentSignature
{
	public void add(int a) // add method with 1 parameter (signature)
	{
		System.out.println(a);
	}
	public void add(int a, int b) // add method with 2 parameters (signature)
	{
		System.out.println(a + " " + b);
	}
	public void add(int a, int b, int c) // add method with 3 parameters (signature)
	{
		System.out.println(a + " " + b + " " + c);
	}
}
